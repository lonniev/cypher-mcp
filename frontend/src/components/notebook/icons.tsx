export function Icon({
  name,
  className = "",
  style,
  title,
  size = 16,
}: {
  name: IconName;
  className?: string;
  style?: CSSProperties;
  title?: string;
  /// Rendered size in px. Decoupled from surrounding font-size so an icon in a
  /// tiny caption isn't itself tiny. Default 16.
  size?: number;
}) {
  const cls = `inline-block shrink-0 align-[-0.15em] ${className}`;
  const L = LUCIDE[name];
  if (L) {
    return <L size={size} className={cls} strokeWidth={2.15} style={style} aria-hidden={title ? undefined : true} aria-label={title} />;
  }
  const b = BRAND[name];
  if (!b) return null;
  return (
    <svg width={size} height={size} viewBox={b.vb ?? "0 0 24 24"} className={`${cls} fill-current`} style={style} role={title ? "img" : "presentation"} aria-label={title} aria-hidden={title ? undefined : true}>
      <path d={b.path} />
    </svg>
  );
}

/// Pick the tech-stack icon for a code symbol from its language / file path.
export function langIcon(lang?: string, file?: string): IconName {
  const l = (lang ?? "").toLowerCase();
  const f = (file ?? "").toLowerCase();
  if (l.includes("python") || f.endsWith(".py")) return "python";
  if (l.includes("typescript") || l === "ts" || f.endsWith(".ts") || f.endsWith(".tsx")) return "typescript";
  if (f.includes("neon") || f.includes("postgres") || f.includes("/vaults/")) return "database";
  if (f.includes("btcpay") || f.includes("lightning") || f.includes("invoice")) return "bolt";
  if (f.includes("nostr") || f.includes("nip04") || f.includes("nip44") || f.includes("identity")) return "key";
  return "symbol";
}
